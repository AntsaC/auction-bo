    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String email = request.getParameter("email");
        String pwd = request.getParameter("pwd");
        Login log = new Login();
        log.setUsername(email);
        log.setPassword(pwd);
        Token tok;
        try{
            tok = (Token)WebClient.postClientLogin("https://auction-production.up.railway.app/login?admin=true", log, new Token());
            if(tok.getData() != null){
                HttpSession session = request.getSession();
                session.setAttribute("token", tok.getData().getValue());
                response.sendRedirect("CategorieController");
            }else{
                 response.sendRedirect("index.jsp?error=error de mot de passe");
            }
            
        }catch(Exception ex){
            try (PrintWriter out = response.getWriter()) {
                ///response.sendRedirect("index.jsp");
                ex.printStackTrace(out);
            }
            
        }        

    }

